// while Loop

let index = 0;
while (index <= 10) {
  // console.log(`Value of index: ${index}`);
  index += 2;
}

let myArray = ["Superman", "Flash", "Thor"];
let arr = 0;
while (arr < myArray.length) {
  //   console.log(`Value is ${myArray[arr]}`);
  arr += 1;
}

// do while loop

let score = 1;
do {
  // console.log(`Score is ${score}`)
  score++;
} while (score <= 10);

let scoree = 11;
do {
  console.log(`Score is ${scoree}`);
  scoree++;
} while (scoree <= 10);
